import java.util.Date;

public class NumberOfPerson {
	private Date title;
	private int quantity;

	public Date getTitle() {
		return title;
	}

	public void setTitle(Date title) {
		this.title = title;
	}

	public int getQuantity() {
		return quantity;
	}

	public void setQuantity(int quantity) {
		this.quantity = quantity;
	}
}
